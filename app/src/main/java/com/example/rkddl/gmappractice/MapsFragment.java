    public void setCenterLocation() {
        isGetCenterLocation = true;
    }

    public void setMarkerCenter() {
        if(isGetCenterLocation) {
            googleMap.clear();
            setMarker(googleMap.getCameraPosition().target.latitude, googleMap.getCameraPosition().target.longitude, 1);
        }
    }

    public void setMarker(double latitude, double longitude, int mode) {
        LatLng loc = new LatLng(latitude, longitude);
        marker = new MarkerOptions().position(loc);
        if(mode == 1) {
            googleMap.setOnMarkerDragListener(this);
            marker.draggable(true);
        } else
            marker.draggable(false);
        googleMap.addMarker(marker);
    }

    public double getMarkerLatitude() {
        if(marker != null)
            return latlngCameraCenter.latitude;
        else
            return 0;
    }

    public double getMarkerLongitude() {
        if(marker != null)
            return latlngCameraCenter.longitude;
        else
            return 0;
    }

    public void setGoogleMapClear() {
        googleMap.clear();
    }

    public void setCurrentLocation(Location location, String markerTitle, String markerSnippet) {
        if ( currentMarker != null ) currentMarker.remove();

        if ( location != null) {
            //현재위치의 위도 경도 가져옴
            LatLng currentLocation = new LatLng( location.getLatitude(), location.getLongitude());

            MarkerOptions markerOptions = new MarkerOptions();
            markerOptions.position(currentLocation);
            markerOptions.title(markerTitle);
            markerOptions.snippet(markerSnippet);
            markerOptions.draggable(true);
            markerOptions.icon(BitmapDescriptorFactory.defaultMarker(BitmapDescriptorFactory.HUE_BLUE));
            currentMarker = this.googleMap.addMarker(markerOptions);

            this.googleMap.moveCamera(CameraUpdateFactory.newLatLng(currentLocation));
            return;
        }

        MarkerOptions markerOptions = new MarkerOptions();
        markerOptions.position(DEFAULT_LOCATION);
        markerOptions.title(markerTitle);
        markerOptions.snippet(markerSnippet);
        markerOptions.draggable(true);
        markerOptions.icon(BitmapDescriptorFactory.defaultMarker(BitmapDescriptorFactory.HUE_RED));
        currentMarker = this.googleMap.addMarker(markerOptions);

        this.googleMap.moveCamera(CameraUpdateFactory.newLatLng(DEFAULT_LOCATION));
    }

